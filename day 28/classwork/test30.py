# ფუნქციის განსაზღვრა
def add(num1, num2):
    result = num1 + num2
    print("The sum of", num1, "and", num2, "is", result)

# ფუნქციის გამოძახება არგუმენტებით
add(10, 5)
add(7, 3)


# ფუნქციის განსაზღვრა
def substract(num1, num2):
    result = num1 - num2
    print("The difference between", num1, "and", num2, "is", result)

# ფუნქციის გამოძახება არგუმენტებით
substract(10, 5)
substract(20, 15)
