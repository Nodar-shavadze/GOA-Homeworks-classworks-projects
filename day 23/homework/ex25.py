# 1) გადააქციეთ string "goa" დიდ ასოებად .upper()-ის გამოყენებით
string1 = "goa"
print(string1.upper())  # Output: "GOA"

# 2) გადააქციეთ string "PROGRAMMING" პატარა ასოებად .lower()-ის გამოყენებით
string2 = "PROGRAMMING"
print(string2.lower())  # Output: "programming"

# 3) გამოიყენეთ ფუნქცია .capitalize() სტრინგზე: "python"
string3 = "python"
print(string3.capitalize())  # Output: "Python"

# 4) .find() ფუნქციის გამოყენებით იპოვეთ ასობგერა "l" სიტყვა "hello"-ში
string4 = "hello"
print(string4.find("l"))  # Output: 2
# Explanation:
# .find() აბრუნებს ასობგერის პირველივე დამთხვევის ინდექსს.
# "hello"-ში ასობგერა "l" პირველად გვხვდება მე-2 პოზიციაზე (ინდექსი 2).

